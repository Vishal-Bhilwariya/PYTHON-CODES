#while loop

i = 0
while i<=5:   #CONDITION
    print(i)   #STATEMENT
    i+=1       #INCREMENT 

# star triangle
j=0
while j<=5:   #CONDITION
    print(j* "*")   #STATEMENT
    j+=1       #INCREMENT     

print(" ")
# star reverse triangle
k=5
while k>=0:   #CONDITION
    print(k* "*")   #STATEMENT
    k-=1       #DECREMENT         

